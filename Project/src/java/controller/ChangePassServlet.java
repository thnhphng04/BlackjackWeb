    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        request.getRequestDispatcher("changepass.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String username = request.getParameter("user");
        
        String oldPass = request.getParameter("oldPass");
        String newPass = request.getParameter("newPass");
        String newPass2 = request.getParameter("newPass2");
        
        PlayerDAO pd = new PlayerDAO();
        
        String realOldPass = pd.getPasswordByUsername(username);
        
        if(oldPass.equals(realOldPass)){
            if(newPass.equals(newPass2)){
                pd.updatePasswordByUserName(username, newPass);
                request.setAttribute("noti", "Đã đổi mật khẩu thành công!");
            }
            else{
                request.setAttribute("noti", "Mật khẩu mới không trùng khớp!");
            }
        }
        else{
            request.setAttribute("noti", "Mật khẩu cũ không đúng!");
        }
        
        request.getRequestDispatcher("changepass.jsp").forward(request, response);
    }

